package start.variable;

public class Var5 {

    public static void main(String[] args) {

        int a; // 변수 선언
        a = 1; // 초기화
        System.out.println("a = " + a);

        int b = 10; // 변수 선언 + 초기화
        System.out.println("b = " + b);

        int c = 3, d = 4; // 여러 변수 선언과 초기화 한번에
        System.out.println("c = " + c);
        System.out.println("d = " + d);
    }
}
