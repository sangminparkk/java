package variable;

public class Var3 {
    public static void main(String[] args) {

        int a; // 변수 선언
        a = 10; // 변수 초기화 (값 할당)

        System.out.println("a = " + a);
        System.out.println("====변수값 변경===");
        a = 50; // 값 변경
        System.out.println("a = " + a);
    }
}
