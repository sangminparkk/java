package variable.ps;

public class VarEx1 {
    public static void main(String[] args) {

        int num1 = 4;
        int num2 = 3;

        // expression
        System.out.println("sum = " + (num1 + num2));
        System.out.println("sub = " + (num1 - num2));
        System.out.println("mul = " + (num1 * num2));
        System.out.println("div = " + (num1 % num2));
    }


}
